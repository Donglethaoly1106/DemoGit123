x=int(input())
y=int(input())
z=int(input())
DTB=float(((x*2)+(y*3)+z)/6)
if DTB<3:
    print('Kem')
if 3<=DTB<5:
    print('Yeu')
if 5<=DTB<6:
    print('Trung binh')
if 6<=DTB<7:
    print('Trung binh Kha')
if 7<=DTB<8:
    print('Kha')
if 8<=DTB<9:
    print('Gioi')
if 9<=DTB<10:
    print('Xuat sac')